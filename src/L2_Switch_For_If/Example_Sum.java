package L2_Switch_For_If;

public class Example_Sum {
    public static void main(String[] args) {
        //calculate and print to console sum of numbers from 10 to 30
        int sum = 0;
        for(int i = 10; i <= 30; i++){
           sum = sum + i;
        }
        System.out.println(sum);
    }
}
